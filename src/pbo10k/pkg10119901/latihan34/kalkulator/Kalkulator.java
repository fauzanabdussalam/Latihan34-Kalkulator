package pbo10k.pkg10119901.latihan34.kalkulator;

/**
 *
 * @author User
 */
public class Kalkulator 
{
    public static double tambahBilangan(Double value1, Double value2)
    {
       return value1+value2;
    }
    
    public static double kurangBilangan(Double value1, Double value2)
    {
       return value1-value2;
    }
    
    public static double kaliBilangan(Double value1, Double value2)
    {
       return value1*value2;
    }
    
    public static double bagiBilangan(Double value1, Double value2)
    {
       return value1/value2;
    }
    
    public static double sisaBilangan(Double value1, Double value2)
    {
       return value1%value2;
    }
}
